
def area(a):
    return a * a
    """
    Вычисляет площадь квадрата по длине его стороны.
    
    Аргументы:
        a (float): Длина стороны квадрата. Должна быть неотрицательной.
    
    Возвращает:
        float: Площадь квадрата.
        
    Пример вызова:
        >>> area(5)
        25
        >>> area(2.5)
        6.25
    """

def perimeter(a):
    return 4 * a
